package com.mehar.openweathermap.weatherpollution.dto;

public class Coordinate {
	
	private float lat;
	private float lon;
	
	public float getLat() {
		return lat;
	}
	public void setLat(float lat) {
		this.lat = lat;
	}
	public float getLon() {
		return lon;
	}
	public void setLon(float lon) {
		this.lon = lon;
	}
	
	
}
